package com.training.primitivetypes;

public class TypeDeclaration {
	public static void main(String[] args) {
		// local variable
		int a; // declaration
		a = 10; // initialization
		int b = 20;
		System.out.println(a + b);
		int c = 30;
		int d = 40;
		System.out.println(c + d);
		
	}
	public static void add() {
		System.out.println("Adding");
	}

}
